// ===== i18n Engine =====

const i18n = {
  currentLang: 'en',

  init() {
    const saved = localStorage.getItem('dialin_lang');
    this.currentLang = saved || this._detectLang();
    this.apply();
  },

  _detectLang() {
    const nav = navigator.language || navigator.userLanguage || 'en';
    return nav.startsWith('zh') ? 'zh' : 'en';
  },

  toggle() {
    this.currentLang = this.currentLang === 'en' ? 'zh' : 'en';
    localStorage.setItem('dialin_lang', this.currentLang);
    this.apply();
    return this.currentLang;
  },

  t(key, params) {
    const keys = key.split('.');
    let val = LANG[this.currentLang];
    for (const k of keys) {
      if (val && typeof val === 'object' && k in val) {
        val = val[k];
      } else if (val && val[k] !== undefined) {
        val = val[k];
      } else {
        return key; // fallback
      }
    }
    if (typeof val === 'string' && params) {
      Object.entries(params).forEach(([k, v]) => {
        val = val.replace(`{${k}}`, v);
      });
    }
    return val;
  },

  getLang() {
    return this.currentLang;
  },

  apply() {
    const lang = this.currentLang;

    // Language toggle button
    document.querySelectorAll('.lang-switch').forEach(el => {
      el.textContent = LANG[lang].langSwitch;
    });

    // Hero
    const heroTitle = document.querySelector('.hero-title');
    if (heroTitle) {
      heroTitle.textContent = LANG[lang].heroTitle;
      heroTitle.setAttribute('data-text', LANG[lang].heroTitle);
    }
    const heroSub = document.querySelector('.hero-subtitle');
    if (heroSub) heroSub.textContent = LANG[lang].heroSubtitle;

    // Mode buttons
    this._applyModeBtn(0, 'modeFree');
    this._applyModeBtn(1, 'modeDaily');
    this._applyModeBtn(2, 'modeDuel');

    // Home stats
    document.querySelectorAll('.stat-label').forEach(el => {
      const key = el.dataset.i18n;
      if (key) el.textContent = this.t(key);
    });

    // Game labels
    const previewLabel = document.querySelector('.preview-label');
    if (previewLabel) previewLabel.textContent = LANG[lang].labelYourSelection;

    const dialInBtn = document.getElementById('btn-dial-in');
    if (dialInBtn) dialInBtn.textContent = LANG[lang].btnDialIn;

    // Results buttons
    document.querySelectorAll('.results-actions .neon-btn').forEach(btn => {
      const key = btn.dataset.i18n;
      if (key) btn.textContent = this.t(key);
    });

    // Footer
    const footerBrand = document.querySelector('.footer-brand');
    if (footerBrand) footerBrand.textContent = LANG[lang].footerBrand;

    // Daily page
    const dailyTitle = document.querySelector('.daily-header h2');
    if (dailyTitle) dailyTitle.textContent = LANG[lang].dailyTitle;
    const dailyPlay = document.getElementById('btn-daily-play');
    if (dailyPlay && !dailyPlay.disabled) dailyPlay.textContent = LANG[lang].dailyPlay;

    // Update HTML lang attribute
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
  },

  _applyModeBtn(index, prefix) {
    const btns = document.querySelectorAll('.mode-grid .neon-btn');
    if (!btns[index]) return;
    const icon = btns[index].querySelector('.btn-icon');
    const text = btns[index].querySelector('.btn-text');
    const sub = btns[index].querySelector('.btn-sub');
    if (icon) icon.textContent = LANG[this.currentLang][prefix + 'Icon'];
    if (text) text.textContent = LANG[this.currentLang][prefix + 'Text'];
    if (sub) sub.textContent = LANG[this.currentLang][prefix + 'Sub'];
  }
};
